	public static int countKdivPairs(int A[], int n, int K) {

		int remain[] = new int[60];

		// Count occurrences of all remainders
		for (int i = 0; i < n; i++) {
			++remain[A[i] % K];
		}

		int sum = remain[0] * (remain[0] - 1) / 2;

		for (int i = 1; i <= K / 2 && i != (K - i); i++)
			sum += remain[i] * remain[K - i];

		if (K % 2 == 0)
			sum += (remain[K / 2] * (remain[K / 2] - 1) / 2);

		return sum;
	}
